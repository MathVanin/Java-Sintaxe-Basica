package edu.mathvanin.javasintaxebasica.operacoes;

public class OperacoesBasicas {
    public static Double somar(Double[] valores) {
        return valores[0] + valores[1];
    }

    public static Double subtrair(Double[] valores) {
        return valores[0] - valores[1];
    }

    public static Double multiplicar(Double[] valores) {
        return valores[0] * valores[1];
    }

    public static Double dividir(Double[] valores) {
        return valores[0] / valores[1];
    }
}
